public class If2 {
    public static void main(String[] args) {
        int nilai1 = 50;
        int nilai2 = 75;
        if (nilai1 == nilai2)
            System.out.println(nilai1+" = "+nilai2);
        if (nilai1 >= nilai2)
            System.out.println(nilai1+" >= "+nilai2); 
        if (nilai1 <= nilai2)
            System.out.println(nilai1+" <= "+nilai2);
        if (nilai1 != nilai2)
            System.out.println(nilai1+" != "+nilai2);
    }
}

