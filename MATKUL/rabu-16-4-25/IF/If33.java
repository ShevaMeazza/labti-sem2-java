public class If33 {
    public static void main(String[] args) {
        int nilai = 90;
        if (nilai >= 90)
            System.out.println("grade : A");
        else if (nilai >= 80)
            System.out.println("grade : B");
        else if (nilai >= 70)
            System.out.println("grade : C");
        else if (nilai >= 60)
            System.out.println("grade : D");
        else if (nilai >= 50)
            System.out.println("grade : D");
    }    
}

