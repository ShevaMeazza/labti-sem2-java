public class Relasi {
    public static void main(String[] args) {
        int A = 10;
        int B = 4;
        System.out.println("Operator Logika");
        System.out.println("A > B = " + (A > B));
        System.out.println("A < B = " + (A < B));
        System.out.println("A = B = " + (A == B));
        System.out.println("A != B= " + (A!=B));
    }
}
