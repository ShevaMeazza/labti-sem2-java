public class increment {
    public static void main(String[] args) {
        int a = 4;
        System.out.println("nilai a adalah 4");

        System.out.println("nilai a menjadi = "+ (a++));
        System.out.println("nilai a menjadi = "+ (++a));

        System.out.println();
        System.out.println("nilai a menjadi = "+ (a--));
       System.out.println("nilai a menjadi = "+ (--a));
    }
}

